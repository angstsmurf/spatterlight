int DrawAtari8ImageFromData(uint8_t *ptr, size_t datasize)
{
    int work,work2;
    int c;
    int i,j;

    uint8_t *origptr = ptr;

    PrintFirstTenBytes(ptr, 0);

    x = 0; y = 0;

    ptr += 2;

    /* set up the palette */
    //    for (i=0;i<256;i++)
    //    {
    //        colours[i].r=0; colours[i].g=0;colours[i].b=0;
    //    }
    //    setuppalette();

    work = *ptr++;
    size = work+(*ptr++ * 256);
    fprintf(stderr, "size: %d (%x)\n",size,size);

    // Get the offsets
    xoff = *ptr++ - 3;
    if (xoff < 0) {
        xoff = 0;
    }
    yoff = *ptr++;
    x = xoff * 8;
    y = yoff;
    fprintf(stderr, "xoff: %d yoff: %d\n",xoff,yoff);
    
    // Get the x length
    xlen = *ptr++;
    ylen = *ptr++;
    fprintf(stderr, "xlen: %d ylen: %d\n",xlen,ylen);

    // Get the palette
    debug_print("Colours: ");
    for (i=1;i<4;i++)
    {
        work=*ptr++;
        SetColour(i,&colours[work]);
        debug_print("%d (0x%02x, 0x%02x, 0x%02x) ", work, colours[work][0], colours[work][1], colours[work][2]);
    }
    work = *ptr++;
    SetColour(0,&colours[work]);

    debug_print("%d\n",work);
    
    //fseek(infile,offset+10,SEEK_SET);
    j=0;
    while (ptr - origptr < datasize - 3)
    {
        // First get count
        c = *ptr++;
        
        if ((c & 0x80) == 0x80) // || countflag)
        { // is a counter
//            if (!countflag)
                c &= 0x7f;
//            if (countflag) c-=1;
            work=*ptr++;
            work2=*ptr++;
            for (i = 0; i < c + 1 && ptr - origptr < datasize - 1; i++)
            {
                DrawC64Pixels(work,work2);
            }
        }
        else
        {
            // Don't count on the next j characters
            
            for (i = 0; i < c + 1 && ptr - origptr < datasize - 1; i++)
            {
                work = *ptr++;
                work2 = *ptr++;
                DrawC64Pixels(work,work2);
            }
        }
    }
    return 1;
}



